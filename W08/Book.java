// 설계도 , 붕어빵 틀
public class Book {

    // 1. 속성, 특징, 변수, 명사
    String title;
    String author;

    // 2. 생성자, 클래스 이름과 동일, 2개 작성
    // 2-1. 기본 생성자
    Book() {}
    // 2-2. 처음 값을 넣어주면서 객체를 하나 만들어주는 생성자
    Book(String title, String author) {
        this.title = title;
        this.author = author;
    }

    // 3. 메소드 동작

}
